#Cadenas de texto (str)= String:
"""Representan secuencias de caracteres, como palabras o frases. Se definen usando comillas simples (') o dobles ("). Las cadenas pueden contener letras, números, y símbolos."""

nombre_apellido = "Marcelo Crisóstomo" #Asignamos la cadena de texto "Juan" a la variable 'nombre'

#Cómo puedo imprimir un valor por consola?
