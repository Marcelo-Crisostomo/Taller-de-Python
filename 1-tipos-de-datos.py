#NÚMERO FLOTANTE (float):
""" Representan números con decimales, como 3.14, -0.001, 2.0. Son útiles para cálculos más precisos que involucran fracciones."""

altura = 1.75  # 'altura' es una variable que almacena un número con decimales
print("La altura es:", altura, "metros")  # Esto imprime la altura seguida de la palabra "metros"

#BOOLEANOS (bool):
""" Solo pueden tomar dos valores, True o False."""
